@app.route('/api/generate/<image_id>', methods=['POST'])
def put_tagged_image(image_id):
    request_url = 'https://' + user + ':' + password + '@' + user + '.cloudant.com/images/'+image_id+'/'+ATTACHMENT_CONSTANT
    r = requests.get(request_url)
    temp_file = image_id+'.jpg'
    f = open(temp_file, "wb")
    f.write(r.content)
    
    
    image = Image.open(temp_file)
    # the array based representation of the image will be used later in order to prepare the
    # result image with boxes and labels on it.
    image_np = load_image_into_numpy_array(image)
    # Expand dimensions since the model expects images to have shape: [1, None, None, 3]
    image_np_expanded = np.expand_dims(image_np, axis=0)
    # Actual detection.
    output_dict = run_inference_for_single_image(image_np, detection_graph)
    # Visualization of the results of a detection.
    vis_util.visualize_boxes_and_labels_on_image_array(
        image_np,
        output_dict['detection_boxes'],
        output_dict['detection_classes'],
        output_dict['detection_scores'],
        category_index,
        instance_masks=output_dict.get('detection_masks'),
        use_normalized_coordinates=True,
        line_thickness=4)
    plt.figure(figsize=IMAGE_SIZE)
    tagged_temp_file = image_id+'_tagged.jpg'
    plt.imsave(tagged_temp_file,image_np)
    
    tf = open(tagged_temp_file, "r")
    attachment = b64encode(tf.read())
    data = {
    '_id': image_id+'_tagged', # Setting _id is optional
    '_attachments': {
        ATTACHMENT_CONSTANT : {
            'data': attachment
            }
        }
    }
    
    doc = db.create_document(data)
    print("done")

